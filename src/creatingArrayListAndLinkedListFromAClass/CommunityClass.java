package creatingArrayListAndLinkedListFromAClass;

public class CommunityClass {
    String neighborName;
    int neighborAge;
    String neighborEthnicity;
    char neighborReligion;

    CommunityClass(String neighborName,int neighborAge,String neighborEthnicity,char neighborReligion){
        this.neighborName = neighborName;
        this.neighborAge = neighborAge;
        this.neighborEthnicity = neighborEthnicity;
        this.neighborReligion = neighborReligion;
    }


}
